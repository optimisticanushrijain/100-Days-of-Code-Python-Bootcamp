# Subscripting
#Pulling out a particular character or element from the string]
# 0 1 2 3 4
# H e l l o
#-5 -4 -3 -2 -1

print("Hello"[4]) # o/p = o
print("Hello"[-5]) # o/p = H
print("Hello"[0]) # o/p = H

# String
print("123" + "345") # o/p = 123345 - #Concatenation

# Integer(+ve or -ve both) = Whole number
print(123 + 345) # o/p = 468

# Large Integers
print(123_456_789) # o/p = 123456789

# Float = Floating Point Number
print(3.14159) # o/p = 3.14159

# Boolean
print(True) # o/p = True
print(False) # o/p = False

